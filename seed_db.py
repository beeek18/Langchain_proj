SEEDED_FILE = "seeded.txt"
QDRANT_PATH = "./qdrant_db"
COLLECTION_NAME = "recipes"


def load_seeded() -> set[str]:
    p = Path(SEEDED_FILE)
    return set(p.read_text(encoding="utf-8").splitlines()) if p.exists() else set()


def mark_seeded(query: str):
    with open(SEEDED_FILE, "a", encoding="utf-8") as f:
        f.write(query + "\n")


async def main():
    import nest_asyncio
    nest_asyncio.apply()

    from qdrant_client import QdrantClient
    from qdrant_client.models import Distance, VectorParams
    from langchain_qdrant import QdrantVectorStore
    from langchain_huggingface import HuggingFaceEmbeddings
    from scraper import scrape

    # Подключаем БД
    client = QdrantClient(path=QDRANT_PATH)
    if not client.collection_exists(COLLECTION_NAME):
        client.create_collection(
            collection_name=COLLECTION_NAME,
            vectors_config=VectorParams(size=384, distance=Distance.COSINE),
        )
        print(f"✅ Коллекция создана")

    device = "cuda" if torch.cuda.is_available() else "mps" if torch.backends.mps.is_available() else "cpu"
    embeddings_model = HuggingFaceEmbeddings(
        model_name="intfloat/multilingual-e5-small",
        model_kwargs={"device": device},
        encode_kwargs={"normalize_embeddings": True},
    )
    vector_store = QdrantVectorStore(
        client=client,
        collection_name=COLLECTION_NAME,
        embedding=embeddings_model,
    )

    seeded = load_seeded()
    todo = [d for d in POPULAR_DISHES if d not in seeded]

    print(f"📊 Всего блюд: {len(POPULAR_DISHES)}")
    print(f"✅ Уже готово: {len(seeded)}")
    print(f"⏳ Осталось: {len(todo)}")
    print()

    for i, query in enumerate(todo, 1):
        print(f"[{i}/{len(todo)}] {query}")
        try:
            recipes = await scrape(query, vector_store, limit=10)
            mark_seeded(query)
            await asyncio.sleep(random.uniform(3, 6))
            count = client.count(collection_name=COLLECTION_NAME).count
            print(f"  ✅ +{len(recipes)} рецептов | всего в БД: {count}")
        except Exception as e:
            print(f"  ❌ Ошибка: {e}")

    print(f"\n🎉 Готово! В БД {client.count(collection_name=COLLECTION_NAME).count} рецептов")


if __name__ == "__main__":
    asyncio.run(main())
